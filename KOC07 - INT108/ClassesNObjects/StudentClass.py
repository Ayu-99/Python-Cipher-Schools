class Student:
    def __init__(self, rollNo, name, marks):
        self.rollNo = rollNo
        self.name = name
        self.marks = marks

    def print(self):
        print(self.rollNo , self.name, self.marks)


#how to create object
student1 = Student(1, "Ayushi", 25)
student1.print()

#how to create object
student2 = Student(2, "Ashima", 30)
student2.print()





